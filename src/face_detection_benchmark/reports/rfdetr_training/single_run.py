"""Single-run RF-DETR training report writers."""

from __future__ import annotations

import csv
from pathlib import Path

from face_detection_benchmark.reports.rfdetr_training.charts import (
    metric_columns_present,
    write_metric_chart,
)
from face_detection_benchmark.reports.rfdetr_training.parsing import (
    csv_value,
    format_float,
    lr_columns,
    parse_rfdetr_training_metrics,
    select_best_rfdetr_training_row,
)
from face_detection_benchmark.reports.rfdetr_training.types import (
    LOSS_COLUMNS,
    MAP_COLUMNS,
    SCORE_COLUMNS,
    RfdetrTrainingMetrics,
    RfdetrTrainingReport,
)


def _count_rows_with_prefix(
    metrics_rows: list[dict[str, float | int]],
    column_prefix: str,
) -> int:
    """Count rows that contain any column with the given prefix."""
    return sum(
        1
        for metrics_row in metrics_rows
        if any(column_name.startswith(column_prefix) for column_name in metrics_row)
    )


def _best_row_summary_lines(best_row: dict[str, float | int]) -> list[str]:
    """Build Markdown rows for important best-row metrics."""
    summary_lines = []
    for column_name in (
        "val/F2",
        "val/precision",
        "val/recall",
        "val/F1",
        "val/mAP_50",
        "val/mAP_50_95",
        "train/loss",
        "val/loss",
    ):
        if column_name in best_row:
            summary_lines.append(
                f"| `{column_name}` | {format_float(best_row[column_name])} |"
            )
    return summary_lines


def _markdown_value(value: float | int | None) -> str:
    """Format one Markdown metric table cell."""
    if value is None:
        return ""
    if isinstance(value, int):
        return str(value)
    return format_float(value)


def _generated_artifact_lines(metrics: RfdetrTrainingMetrics) -> list[str]:
    """List the files generated by a single-run training report."""
    artifact_lines = [
        "- `metrics_clean.csv`",
        "- `metrics.md`",
        "- `summary.md`",
        "- `training_validation_loss.svg`",
        "- `validation_precision_recall_f1.svg`",
        "- `map.svg`",
    ]
    if lr_columns(metrics.columns):
        artifact_lines.append("- `learning_rate.svg`")
    return artifact_lines


def write_rfdetr_training_metrics_csv(
    metrics: RfdetrTrainingMetrics,
    metrics_clean_path: Path,
) -> None:
    """Write merged RF-DETR training metrics as a cleaned CSV."""
    metrics_clean_path.parent.mkdir(parents=True, exist_ok=True)
    with metrics_clean_path.open("w", encoding="utf-8", newline="") as metrics_file:
        writer = csv.DictWriter(metrics_file, fieldnames=metrics.columns)
        writer.writeheader()
        writer.writerows(
            {
                column_name: csv_value(metrics_row.get(column_name))
                for column_name in metrics.columns
            }
            for metrics_row in metrics.rows
        )


def write_rfdetr_training_metrics_markdown(
    metrics: RfdetrTrainingMetrics,
    metrics_markdown_path: Path,
) -> None:
    """Write merged RF-DETR training metrics as a Markdown table."""
    lines = [
        "# RF-DETR Training Metrics",
        "",
        "Merged RF-DETR `metrics.csv` rows. Blank cells indicate that RF-DETR "
        "did not emit that metric for the epoch/step.",
        "",
        "| " + " | ".join(f"`{column_name}`" for column_name in metrics.columns) + " |",
        "| " + " | ".join("---:" for _ in metrics.columns) + " |",
    ]
    for metrics_row in metrics.rows:
        lines.append(
            "| "
            + " | ".join(
                _markdown_value(metrics_row.get(column_name))
                for column_name in metrics.columns
            )
            + " |"
        )
    metrics_markdown_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_rfdetr_training_summary(
    report: RfdetrTrainingReport,
    summary_path: Path,
) -> None:
    """Write a Markdown summary for one RF-DETR training report."""
    best_row = report.best_row
    lines = [
        "# RF-DETR Training Report",
        "",
        "This report summarizes RF-DETR training and validation telemetry from "
        "`metrics.csv`. It is not benchmark accuracy reporting.",
        "",
        f"Run id: `{report.run_id}`",
        f"Source metrics CSV: `{report.source_metrics_path.as_posix()}`",
        f"Source rows: {report.metrics.source_row_count}",
        f"Merged rows: {report.metrics.merged_row_count}",
        f"Training rows: {_count_rows_with_prefix(report.metrics.rows, 'train/')}",
        f"Validation rows: {_count_rows_with_prefix(report.metrics.rows, 'val/')}",
        f"Selection metric: `{report.selection_metric}` (`{report.selection_column}`)",
        f"Best epoch: {int(best_row['epoch'])}",
        f"Best step: {int(best_row['step'])}",
        "",
        "## Best Validation Row",
        "",
        "| Metric | Value |",
        "| --- | ---: |",
        *_best_row_summary_lines(best_row),
        "",
        "## Cleaned-Dataset Evaluation",
        "",
        "To evaluate a selected checkpoint on the cleaned face dataset, use "
        "`predict-rfdetr-benchmark` followed by `validate-thresholds` or "
        "`evaluate-detections`. Keep those outputs separate under "
        "`runs/validation/` or `runs/benchmarks/`.",
        "",
        "If `data/benchmark/target-video-test-3fps-clean/test` is used for "
        "checkpoint or threshold selection, treat that output as validation "
        "analysis rather than an unbiased final test benchmark.",
        "",
        "## Generated Artifacts",
        "",
        *_generated_artifact_lines(report.metrics),
    ]
    summary_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_rfdetr_training_report(
    metrics_csv_path: Path,
    output_dir: Path,
    run_id: str,
    selection_metric: str = "f2",
) -> dict[str, Path]:
    """Write a single-run RF-DETR training metrics report."""
    metrics = parse_rfdetr_training_metrics(metrics_csv_path)
    best_row, normalized_metric, selection_column = select_best_rfdetr_training_row(
        metrics,
        selection_metric=selection_metric,
    )
    report = RfdetrTrainingReport(
        run_id=run_id,
        source_metrics_path=metrics_csv_path,
        metrics=metrics,
        selection_metric=normalized_metric,
        selection_column=selection_column,
        best_row=best_row,
    )

    output_dir.mkdir(parents=True, exist_ok=True)
    metrics_clean_path = output_dir / "metrics_clean.csv"
    metrics_markdown_path = output_dir / "metrics.md"
    summary_path = output_dir / "summary.md"
    loss_path = output_dir / "training_validation_loss.svg"
    score_path = output_dir / "validation_precision_recall_f1.svg"
    map_path = output_dir / "map.svg"
    learning_rate_path = output_dir / "learning_rate.svg"

    write_rfdetr_training_metrics_csv(metrics, metrics_clean_path)
    write_rfdetr_training_metrics_markdown(metrics, metrics_markdown_path)
    write_rfdetr_training_summary(report, summary_path)
    write_metric_chart(
        title="RF-DETR Training and Validation Loss",
        y_label="Loss",
        metrics_rows=metrics.rows,
        column_names=metric_columns_present(metrics.rows, LOSS_COLUMNS),
        output_path=loss_path,
    )
    write_metric_chart(
        title="RF-DETR Validation Precision, Recall, F1, and F2",
        y_label="Score",
        metrics_rows=metrics.rows,
        column_names=metric_columns_present(metrics.rows, SCORE_COLUMNS),
        output_path=score_path,
        y_domain=(0.0, 1.0),
    )
    write_metric_chart(
        title="RF-DETR Validation mAP",
        y_label="mAP",
        metrics_rows=metrics.rows,
        column_names=metric_columns_present(metrics.rows, MAP_COLUMNS),
        output_path=map_path,
        y_domain=(0.0, 1.0),
    )

    paths = {
        "metrics_clean_path": metrics_clean_path,
        "metrics_markdown_path": metrics_markdown_path,
        "summary_path": summary_path,
        "loss_path": loss_path,
        "score_path": score_path,
        "map_path": map_path,
    }
    learning_rate_columns = lr_columns(metrics.columns)
    if learning_rate_columns:
        write_metric_chart(
            title="RF-DETR Learning Rate",
            y_label="Learning rate",
            metrics_rows=metrics.rows,
            column_names=learning_rate_columns,
            output_path=learning_rate_path,
        )
        paths["learning_rate_path"] = learning_rate_path
    return paths
